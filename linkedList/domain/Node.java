package domain;

/**
 * Created by evan on 2019/4/1.
 *
 * 单链表节点
 */
public class Node {

    public int data;
    public Node next;

    public Node(int data, Node next) {
        this.data = data;
        this.next = next;
    }

    public Node(int data) {
        this.data = data;
        this.next = next;
    }
}
